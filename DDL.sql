-- 강제로라도 부모키에 해당하는 컬럼을 지우고 싶다면 제약조건도 함께 삭제가 되어야 한다. (CASCADE CONSTRAINTS)
ALTER TABLE TB1
DROP COLUMN PK CASCADE CONSTRAINTS;
  
